#!perl
################################################################################
## This package contains some common functions that can be derived from a fold
## (or reduce in perl) of a list.
## Author: Javier Iserte
## Usage:
##   use JAI::Lists::Reduced
################################################################################

################################################################################
## Module name
package JAI::Lists::Reduced;
################################################################################

################################################################################
## Define Modules Needed
use strict;
use warnings;
use version;
use IO::File;
use Getopt::Long;
use Bio::SeqIO;
use Readonly;
use autodie;
use IPC::Open3;
use Data::Dumper::Simple;
use List::Util 'reduce';
use Exporter 'import';
use English qw( -no_match_vars );
################################################################################

################################################################################
## Exports
our @EXPORT_OK = qw(concat);
our %EXPORT_TAGS = ( DEFAULT => [qw(&concat)],
  Both => [qw(&concat)] );
################################################################################

################################################################################
## Constants
Readonly my $CONSTANT => 0;
################################################################################

################################################################################
## Version
our $VERSION = 1.0000;
################################################################################

################################################################################
## concat
sub concat {
  return reduce { $a . $b } @ARG;
}
